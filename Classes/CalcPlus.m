#import "CalcPlus.h"

@implementation CalcPlus

- (NSInteger)plus:(NSInteger)a withB:(NSInteger) b {
    return a + b;
}

- (NSInteger)minus:(NSInteger)a withB:(NSInteger) b {
    return a - b;
}

@end
